public class Stove {
    void on() {
        System.out.println("Stove is on");
    }

    void off() {
        System.out.println("Stove is off");
    }

    void setHeat(int heat) {
        System.out.println("Stove heat set to " + heat);
    }

    void turnOnFan() {
        System.out.println("Stove fan turned on");
    }
}